package Day_54_Collection_Set;

public class Person72 {
    String name;
    int age;

    public Person72( String name, int age) {
        this.age = age;
        this.name = name;
    }

    @Override
    public String toString() {
        return "Person72{" +
                "age=" + age +
                ", name='" + name + '\'' +
                '}';
    }
}
