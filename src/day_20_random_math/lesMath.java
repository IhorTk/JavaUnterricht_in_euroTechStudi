package day_20_random_math;

public class lesMath {
    public static void main(String[] args) {
        double piValue = Math.PI;
        System.out.println(piValue);
        double exponentValue = Math.E;
        System.out.println(exponentValue);
        double sqrtValue = Math.sqrt(25);
        System.out.println(sqrtValue);
        double cbrtValue = Math.cbrt(27);
        System.out.println(cbrtValue);
        double randomValue = Math.random();
        System.out.println(randomValue);
        double powerValue = Math.pow(5, 6);
        System.out.println(powerValue);
        int randomInt = ( (int) (Math.random()*10));
        System.out.println(randomInt);

    }

}
